#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*counter;
	t_size	i;

	counter = lst;
	i = 0;
	while (counter != 0)
	{
		counter = counter->next;
		i++;
	}
	return (i);
}
